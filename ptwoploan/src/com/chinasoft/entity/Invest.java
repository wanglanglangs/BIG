package com.chinasoft.entity;


public class Invest {
	//投资表
	
	private String taccount;//投资人账号名
	private String titem;//投资项目名
	private String tmoney;//投资金额
	private String ttotla;//预计总收益
	private String tstart;//投资开始时间
	private String tend;//投资结束时间
	private String trate;//投资利率
	private long tid;//合同ID
	
	public Invest(long tid, String taccount, String titem, String tmoney,
			String ttotla, String tstart, String tend, String trate) {
		super();
		this.tid = tid;
		this.taccount = taccount;
		this.titem = titem;
		this.tmoney = tmoney;
		this.ttotla = ttotla;
		this.tstart = tstart;
		this.tend = tend;
		this.trate = trate;
	}

	public Invest() {
		super();
	}

	public long getTid() {
		return tid;
	}

	public void setTid(long tid) {
		this.tid = tid;
	}

	public String getTaccount() {
		return taccount;
	}

	public void setTaccount(String taccount) {
		this.taccount = taccount;
	}

	public String getTitem() {
		return titem;
	}

	public void setTitem(String titem) {
		this.titem = titem;
	}

	public String getTmoney() {
		return tmoney;
	}

	public void setTmoney(String tmoney) {
		this.tmoney = tmoney;
	}

	public String getTtotla() {
		return ttotla;
	}

	public void setTtotla(String ttotla) {
		this.ttotla = ttotla;
	}

	public String getTstart() {
		return tstart;
	}

	public void setTstart(String tstart) {
		this.tstart = tstart;
	}

	public String getTend() {
		return tend;
	}

	public void setTend(String tend) {
		this.tend = tend;
	}

	public String getTrate() {
		return trate;
	}

	public void setTrate(String trate) {
		this.trate = trate;
	}

	@Override
	public String toString() {
		return "Invest [taccount=" + taccount + ", titem=" + titem
				+ ", tmoney=" + tmoney + ", ttotla=" + ttotla + ", tstart="
				+ tstart + ", tend=" + tend + ", trate=" + trate + ", tid="
				+ tid + "]";
	}
	

}
